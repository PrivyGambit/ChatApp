import auth, { logout, saveUserToDatabase } from '../../helpers/auth'
import { formatUserInfo } from '../../helpers/utils'
import { fetchUser, incrementSelectedCount, decrementSelectedCount } from '../../helpers/api'

const AUTH_USER = 'AUTH_USER'
const UNAUTH_USER = 'UNAUTH_USER'
const FETCHING_USER = 'FETCHING_USER'
const FETCHING_USER_FAILURE = 'FETCHING_USER_FAILURE'
const FETCHING_USER_SUCCESS = 'FETCHING_USER_SUCCESS'
const REMOVE_FETCHING_USER = 'REMOVE_FETCHING_USER'
const ADD_USER = 'ADD_USER'

export function addUser (user) {
  return {
    type: ADD_USER,
    user,
    lastUpdated: Date.now(),
  }
}

export function authUser (uid) {
  return {
    type: AUTH_USER,
    uid,
  }
}

function unauthUser () {
  return {
    type: UNAUTH_USER,
  }
}

function fetchingUser () {
  return {
    type: FETCHING_USER,
  }
}

function fetchingUserFailure (error) {
  return {
    type: FETCHING_USER_FAILURE,
    error: 'Error fetching user.',
  }
}

export function fetchingUserSuccess (uid, user, timestamp) {
  return {
    type: FETCHING_USER_SUCCESS,
    uid,
    user,
    timestamp
  }
}

export function removeFetchingUser () {
  return {
    type: REMOVE_FETCHING_USER
  }
}

export function fetchDatabaseUserInfo ( uid ) {
    return function () {
        return fetchUser ( uid )
    }
}

export function fetchAndHandleAuthedUser () {
  logoutAndUnauth()
  return function (dispatch) {
    dispatch(fetchingUser())
    return auth().then(({user, credential}) => {
      const userData = user.providerData[0]
      const userInfo = formatUserInfo(userData.displayName, userData.photoURL, user.uid, 'normal')
      return this.props.fetchDatabaseUserInfo(user.uid)
        .then(( dataInfo ) => {
            return dispatch(fetchingUserSuccess(
                dataInfo.uid,
                dataInfo.banned,
                dataInfo.name,
                dataInfo.type,
                Date.now()
            ))
        })

    })
    .then(({user}) => saveUserToDatabase(user))
    .then((user) => dispatch(authUser(user.uid)))
    .catch((error) => dispatch(fetchingUserFailure(error)))
  }
}

export function logoutAndUnauth () {
  return function (dispatch) {
    logout()
    dispatch(unauthUser())
  }
}

const initialUserState = {
  lastUpdated: 0,
  info: {
    name: '',
    uid: '',
    avatar: '',
  }
}

function user (state = initialUserState, action) {
  switch (action.type) {
    case FETCHING_USER_SUCCESS :
      return {
        ...state,
        info: action.user,
        lastUpdated: action.timestamp,
      }
    default :
      return state
  }
}

const initialState = {
  isFetching: true,
  error: '',
  isAuthed: false,
  authedId: ''
}

export default function users (state = initialState, action) {
  switch (action.type) {
    case AUTH_USER :
      return {
        ...state,
        isAuthed: true,
        authedId: action.uid,
      }
    case UNAUTH_USER :
      return {
        ...state,
        isAuthed: false,
        authedId: '',
      }
    case FETCHING_USER :
      return {
        ...state,
        isFetching: true,
      }
    case FETCHING_USER_FAILURE :
      return {
        ...state,
        isFetching: false,
        error: action.error,
      }
    case FETCHING_USER_SUCCESS :
      return action.user === null
        ? {
          ...state,
          error: '',
          isFetching: false,
        }
        : {
          ...state,
          isFetching: false,
          error: '',
          [action.uid]: user(state[action.uid], action)
        }
    case REMOVE_FETCHING_USER :
      return {
        ...state,
        isFetching: false,
      }
    case ADD_USER :
      return typeof state[action.user.uid] !== 'undefined'
        ? state
        : {
          ...state,
          [action.user.uid]: {
            ...state[action.user.uid],
            lastUpdated: action.lastUpdated,
            info: action.user,
          },
        }
    default :
      return state
  }
}
